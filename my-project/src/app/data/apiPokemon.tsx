// Função utilitária para mapear tipos com cores no arquivo "apiPokemonTipos" chave "typeColors"
const mapapiPokemonTiposComCores = (typeColors: string[]) => {
  return typeColors.map((typeColor) => {
    const tipoCorrespondente = apiPokemonTipos.find(
      (encontre) => encontre.type === typeColor
    )
    return tipoCorrespondente ? tipoCorrespondente.color : '#000000' // Retorna preto se não encontrar
  })
}

